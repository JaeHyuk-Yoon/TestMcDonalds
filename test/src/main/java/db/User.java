package db;

/**
 *
 * @author heejin
 */
public class User {
    private String id;
    private String pw;
    private String branch;
    private String type;
    
    public User(){

    }
    
    public User(String id, String pw, String branch, String type) {
        this.id = id;
        this.pw = pw;
        this.branch = branch;
        this.type = type;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getPw() {
        return pw;
    }

    public void setPw(String pw) {
        this.pw = pw;
    }

    public String getBranch() {
        return branch;
    }

    public void setBranch(String branch) {
        this.branch = branch;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
